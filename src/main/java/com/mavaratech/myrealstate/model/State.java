package com.mavaratech.myrealstate.model;

public class State {
    private String arrestText;
    private String basic;
    private boolean hasEstateElectronicNoteNo;
    private String province;
    private String secondary;
    private String section;
    private String sharePart;
    private String shareText;
    private String shareTotal;
    private String subSection;
    private String unitId;
    private String unitName;
    private String plaqueText;

    public String getArrestText() {
        return arrestText;
    }

    public void setArrestText(String arrestText) {
        this.arrestText = arrestText;
    }

    public String getBasic() {
        return basic;
    }

    public void setBasic(String basic) {
        this.basic = basic;
    }

    public boolean isHasEstateElectronicNoteNo() {
        return hasEstateElectronicNoteNo;
    }

    public void setHasEstateElectronicNoteNo(boolean hasEstateElectronicNoteNo) {
        this.hasEstateElectronicNoteNo = hasEstateElectronicNoteNo;
    }

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public String getSecondary() {
        return secondary;
    }

    public void setSecondary(String secondary) {
        this.secondary = secondary;
    }

    public String getSection() {
        return section;
    }

    public void setSection(String section) {
        this.section = section;
    }

    public String getSharePart() {
        return sharePart;
    }

    public void setSharePart(String sharePart) {
        this.sharePart = sharePart;
    }

    public String getShareText() {
        return shareText;
    }

    public void setShareText(String shareText) {
        this.shareText = shareText;
    }

    public String getShareTotal() {
        return shareTotal;
    }

    public void setShareTotal(String shareTotal) {
        this.shareTotal = shareTotal;
    }

    public String getSubSection() {
        return subSection;
    }

    public void setSubSection(String subSection) {
        this.subSection = subSection;
    }

    public String getUnitId() {
        return unitId;
    }

    public void setUnitId(String unitId) {
        this.unitId = unitId;
    }

    public String getUnitName() {
        return unitName;
    }

    public void setUnitName(String unitName) {
        this.unitName = unitName;
    }

    public String getPlaqueText() {
        return plaqueText;
    }

    public void setPlaqueText(String plaqueText) {
        this.plaqueText = plaqueText;
    }
}
